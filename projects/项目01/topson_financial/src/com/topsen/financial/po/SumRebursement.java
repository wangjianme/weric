package com.topsen.financial.po;

public class SumRebursement {
	private int id;
	private float total;
	private float butie;
	private float zongji;
	private String rebId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}
	public float getButie() {
		return butie;
	}
	public void setButie(float butie) {
		this.butie = butie;
	}
	public float getZongji() {
		return zongji;
	}
	public void setZongji(float zongji) {
		this.zongji = zongji;
	}
	public String getRebId() {
		return rebId;
	}
	public void setRebId(String rebId) {
		this.rebId = rebId;
	}
}
